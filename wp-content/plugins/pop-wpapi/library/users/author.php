/**---------------------------------------------------------------------------------------------------------------
 * Return the author of the post (to be overriden by Co-Authors plus)
 * ---------------------------------------------------------------------------------------------------------------*/
function gd_get_postauthors($post_id = null) {

	$post = get_post($post_id);
	
	return apply_filters('gd_get_postauthors', array($post->post_author), $post);
}