define ('GD_TEMPLATE_MESSAGEFEEDBACKINNER_NOTIFICATIONS', PoP_TemplateIDUtils::get_template_definition('messagefeedbackinner-notifications'));

class GD_AAL_Template_Processor_CustomListMessageFeedbackInners extends GD_Template_Processor_MessageFeedbackInnersBase {

	function get_templates_to_process() {
	
		return array(
			GD_TEMPLATE_MESSAGEFEEDBACKINNER_NOTIFICATIONS,
		);
	}

	function get_layouts($template_id) {

		$ret = parent::get_layouts($template_id);

		$layouts = array(
			GD_TEMPLATE_MESSAGEFEEDBACKINNER_NOTIFICATIONS => GD_TEMPLATE_LAYOUT_MESSAGEFEEDBACKFRAME_NOTIFICATIONS,
		);

		if ($layout = $layouts[$template_id]) {

			$ret[] = $layout;
		}

		return $ret;
	}
}


/**---------------------------------------------------------------------------------------------------------------
 * Initialization
 * ---------------------------------------------------------------------------------------------------------------*/
new GD_AAL_Template_Processor_CustomListMessageFeedbackInners();